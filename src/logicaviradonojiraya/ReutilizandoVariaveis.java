public class ReutilizandoVariaveis {

    public static void main(String[] args) {

        double salario = 5000;
        double resutado = salario * 0.3;

        System.out.println("30% eh: " + resutado);

        resutado = salario * 0.15;
        System.out.println("15% eh: " + resutado);

        resutado = salario * 0.05;
        System.out.println("5% eh: " + resutado);

    }
}
